// app.js
App({
  onLaunch: function () {
    this.globalData = {
      // env 参数说明：
      // env 参数决定接下来小程序发起的云开发调用（wx.cloud.xxx）会请求到哪个云环境的资源
      // ⚠️ 此处请填入你的云环境 ID（开发者工具右上「云开发」面板里可看到，形如 cloud1-xxxxxxxx）
      env: "cloud1-d0g0q2bpqcb772849",
    };
    if (!wx.cloud) {
      console.error("请使用 2.2.3 或以上的基础库以使用云能力");
    } else {
      wx.cloud.init({
        env: this.globalData.env,
        traceUser: true,
      });
    }
  },
});
